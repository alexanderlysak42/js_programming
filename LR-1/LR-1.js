// Перевірка логіну та паролю

let login = prompt("Введіть логін:");

if (login === null || login === "") {
    // Користувач натиснув ESC або нічого не ввів
    alert("Скасовано");

} else if (login === "Admin") {
    // Логін правильний — запитуємо пароль
    let password = prompt("Введіть пароль:");

    if (password === null || password === "") {
        // ESC або порожній рядок
        alert("Скасовано");

    } else if (password === "Господар") {
        alert("Ласкаво просимо!");

    } else {
        alert("Неправильний пароль");
    }

} else {
    // Введено інший логін, не "Admin"
    alert("Я вас не знаю");
}